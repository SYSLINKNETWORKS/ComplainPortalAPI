declare @men_id int,@usr_id int
declare usr cursor for 
select usr_id from new_usr --where usr_id not in (1)
OPEN usr
		FETCH NEXT FROM usr
		INTO @usr_id
			WHILE @@FETCH_STATUS = 0
			BEGIN				
				declare men cursor for 
				select men_id from m_men where men_id not in (select men_id from m_per where usr_id=@usr_id)
				OPEN men
						FETCH NEXT FROM men
						INTO @men_id
							WHILE @@FETCH_STATUS = 0
							BEGIN				
							exec ins_m_per '01','01',1,1,1,1,1,@men_id,@usr_id,'','',1,''
							FETCH NEXT FROM men
									INTO @men_id

					end
					CLOSE men
					DEALLOCATE men
			FETCH NEXT FROM usr
					INTO @usr_id

	end
	CLOSE usr

	DEALLOCATE usr
